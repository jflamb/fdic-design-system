function formatLiteral(value) {
  if (value === undefined || value === null) return "`undefined`";
  if (typeof value === "string") return `\`${value}\``;
  if (typeof value === "boolean" || typeof value === "number") {
    return `\`${String(value)}\``;
  }

  return `\`${String(value)}\``;
}

function sortComponentsForDocs(components) {
  return [...components].sort((left, right) => {
    const orderDelta = (left.docs.order ?? Number.MAX_SAFE_INTEGER) -
      (right.docs.order ?? Number.MAX_SAFE_INTEGER);

    if (orderDelta !== 0) {
      return orderDelta;
    }

    return left.docs.title.localeCompare(right.docs.title);
  });
}

function groupStandaloneComponentsForDocs() {
  return DOCS_CATEGORY_METADATA.map((category) => ({
    ...category,
    components: sortComponentsForDocs(
      standaloneDocComponents.filter(
        (component) => component.docs.category === category.key,
      ),
    ),
  })).filter((category) => category.components.length > 0);
}

