function generateRegisterFile(component) {
  const dependencyLines = component.register.dependencies
    .map((dependency) => {
      const dependencyComponent = componentInventory.find(
        (candidate) => candidate.tagName === dependency,
      );

      return `import { ${dependencyComponent.className} } from "../components/${dependencyComponent.sourceFile.replace(/\.ts$/, ".js")}";`;
    })
    .join("\n");

  const dependencyDefinitions = component.register.dependencies
    .map(
      (dependency) => `if (!customElements.get("${dependency}")) {
  customElements.define("${dependency}", ${componentInventory.find((candidate) => candidate.tagName === dependency).className});
}`,
    )
    .join("\n\n");

  return `// ${GENERATED_HEADER}
import { ${component.className} } from "../components/${component.sourceFile.replace(/\.ts$/, ".js")}";
${dependencyLines ? `${dependencyLines}\n` : ""}
${dependencyDefinitions ? `${dependencyDefinitions}\n\n` : ""}if (!customElements.get("${component.tagName}")) {
  customElements.define("${component.tagName}", ${component.className});
}
`;
}

